//Method without parameter
function addition() {
    let a = 10;
    let b = 22
    console.log(a+b);
}
addition()

//Method with paramter
function substraction(a, b) {
    console.log(a-b);
}
substraction(22, 12)

//With parameter and return type
function remainder(a, b) {
    return a%b
}
console.log(remainder(55, 10))